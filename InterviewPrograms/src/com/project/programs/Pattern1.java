package com.project.programs;
/*to print pattern 
 * 1  2  3  4
 * 5  6  7  8
 * */

public class Pattern1 {
	public static void main(String[] args) {
		for (int i = 1; i <= 4; i++) {
			System.out.print(i);
		}
		System.out.println();

		for (int i = 5; i <= 8; i++) {
			System.out.print(i);
		}
	}

}
